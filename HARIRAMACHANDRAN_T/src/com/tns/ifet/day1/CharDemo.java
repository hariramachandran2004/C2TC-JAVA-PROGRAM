package com.tns.ifet.day1;

public class CharDemo {
	public static void main(String[] args) {
		char c1='a';
		System.out.println(c1);
		char c2=65;
		System.out.println(c2);
		char var1='\u00A7';
		System.out.println(var1);
		char var2='\u20AC';
		System.out.println(var2);
		int a='A';
		System.out.println(a);
	}
}
